- (void)viewDidLoad {
    [super viewDidLoad];
    
    self.title=@"设置中心";
    
    NSString *httpUrl = @"http://apis.baidu.com/3023/qq/qq";
    NSString *httpArg = [NSString stringWithFormat:@"uins=%@",[UserInfonCache sharedUserInfonCache].qqNum];
    [self request: httpUrl withHttpArg: httpArg];
    

    
}

-(void)viewWillAppear:(BOOL)animated{

    if ([UserInfonCache sharedUserInfonCache].userImage) {
        self.userImage.image=[UserInfonCache sharedUserInfonCache].userImage;
    }
    if ([UserInfonCache sharedUserInfonCache].nickName) {
        self.userName.text=[UserInfonCache sharedUserInfonCache].nickName;
    }
}


-(void)request: (NSString*)httpUrl withHttpArg: (NSString*)HttpArg  {
    NSString *urlStr = [[NSString alloc]initWithFormat: @"%@?%@", httpUrl, HttpArg];
    NSURL *url = [NSURL URLWithString: urlStr];
    NSMutableURLRequest *request = [[NSMutableURLRequest alloc]initWithURL: url cachePolicy: NSURLRequestUseProtocolCachePolicy timeoutInterval: 10];
    [request setHTTPMethod: @"GET"];
    [request addValue: BDAPIKey forHTTPHeaderField: @"apikey"];
    [NSURLConnection sendAsynchronousRequest: request
                                       queue: [NSOperationQueue mainQueue]
                           completionHandler: ^(NSURLResponse *response, NSData *data, NSError *error){
                               if (error) {
                                   NSLog(@"Httperror: %@%ld", error.localizedDescription, error.code);
                               } else {
                                   NSInteger responseCode = [(NSHTTPURLResponse *)response statusCode];
                                   NSString *responseString = [[NSString alloc] initWithData:data encoding:NSUTF8StringEncoding];
                                   NSLog(@"HttpResponseCode:%ld", responseCode);
                                   NSLog(@"HttpResponseBody %@",responseString);
                               }
                           }];
}



